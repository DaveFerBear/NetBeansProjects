package ferris_david_chess_submission6;

import java.awt.image.BufferedImage;

/**
 *
 * @author davidferris
 */
public abstract class Piece {
//abstract class definining methods in various piece classes
    BufferedImage pieceImage; //every piece must have an image

    abstract int getX();
    abstract int getY();
    abstract void setLocation(int newX, int newY);
    abstract BufferedImage getImage();
    abstract boolean isLegalMove(Piece piece, int newX, int newY);
    abstract boolean getColor();
    abstract String getKind();
}

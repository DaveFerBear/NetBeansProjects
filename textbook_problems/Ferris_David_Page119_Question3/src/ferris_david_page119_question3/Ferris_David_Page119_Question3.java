public class Ferris_David_Page119_Question3 {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        
        int [] randomArray = new int[500]; //Creates the array
        
        System.out.format("%-5s %-5s %s", "Index", "Occurances", "\n");
        
        for (int x = 0; x < 500; x++) //loops 500 times
        {
            randomArray[(int)(Math.random()*10)]++; //counts for every time a number is generated
        }
        for (int x = 0; x < 10; x++) //loops 10 times
        {
            System.out.format("%-5s %-4s %s", x, randomArray[x], "\n"); //prints each array element in order
        }
    }
}
